export const getCatalogProduct = (value: string): ProductDictionaryEntry | undefined => {
  const normalizedValue = normalizeProductName(value);
  const exactMatch = productDictionary[normalizedValue];

  if (exactMatch) {
    return exactMatch;
  }

  return Object.entries(productDictionary).find(([dictionaryName]) =>
    normalizedValue.startsWith(`${dictionaryName} `),
  )?.[1];
};

export const getDefaultCategory = (categoryId: string): ShoppingCategory | undefined =>
  defaultCategories.find((category) => category.id === categoryId);
